#include "3-calc.h"
/**
 * op_add - finds the sum of 2 integers
 * @a: int 1
 * @b: int 2
 *
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - finds the difference
 * @a: int 1
 * @b: int 2
 *
 * Return: the difference
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - finds the product
 * @a: int 1
 * @b: int 2
 *
 * Return: the product
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - Divides
 * @a: int 1
 * @b: int 2
 *
 * Return: divided answer
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - finds the rem after dividing
 * @a: int 1
 * @b: int 2
 *
 * Return: the remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
